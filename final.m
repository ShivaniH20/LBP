function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%% Feature Extraction

J1=handles.J1;
J2=handles.J2;
J3=handles.J3;

%% R- Channel Feature Extraction Using Local Binary Pattern
I1=J1;

  w=size(I1,1);     
   h=size(I1,2);  

%% LBP    
 scale = 2.^[7 6 5; 0 -inf 4; 1 2 3];  
    for i=2:w-1   
      for j=2:h-1    
         J0=I1(i,j);   
          I3(i-1,j-1)=I1(i-1,j-1)>J0;  
           I3(i-1,j)=I1(i-1,j)>J0;   
          I3(i-1,j+1)=I1(i-1,j+1)>J0;  
            I3(i,j+1)=I1(i,j+1)>J0;     
        I3(i+1,j+1)=I1(i+1,j+1)>J0;    
          I3(i+1,j)=I1(i+1,j)>J0;      
        I3(i+1,j-1)=I1(i+1,j-1)>J0;     
         I3(i,j-1)=I1(i,j-1)>J0;       
      I1(i-1,j-1)=I3(i-1,j-1)*2^7+I3(i-1,j)*2^6+I3(i-1,j+1)*2^5+I3(i,j+1)*2^4+I3(i+1,j+1)*2^3+I3(i+1,j)*2^2+I3(i+1,j-1)*2^1+I3(i,j-1)*2^0;

      end 
    end 
   
    axes(handles.axes11);
    imshow(I1);
    title('R -LBP Image');
    
    
% Texture Features
      
Mean1 = mean2(I1);
set(handles.edit8,'string',Mean1);
Standard_Deviation2 = std2(I1);
set(handles.edit9,'string',Standard_Deviation2 );
Entropy3 = entropy(I1);
set(handles.edit10,'string',Entropy3);
Kurtosis4 = kurtosis(double(I1(:)));
set(handles.edit11,'string',Kurtosis4);
Skewness5 = skewness(double(I1(:)));
set(handles.edit12,'string',Skewness5);

%% G Channel -Feature Extraction Using Local Binary Pattern
I2=J2;

  w=size(I2,1);     
   h=size(I2,2);  

%% LBP    
 scale = 2.^[7 6 5; 0 -inf 4; 1 2 3];  
    for i=2:w-1   
      for j=2:h-1    
         J0=I2(i,j);   
          I3(i-1,j-1)=I2(i-1,j-1)>J0;  
           I3(i-1,j)=I2(i-1,j)>J0;   
          I3(i-1,j+1)=I2(i-1,j+1)>J0;  
            I3(i,j+1)=I2(i,j+1)>J0;     
        I3(i+1,j+1)=I2(i+1,j+1)>J0;    
          I3(i+1,j)=I2(i+1,j)>J0;      
        I3(i+1,j-1)=I2(i+1,j-1)>J0;     
         I3(i,j-1)=I2(i,j-1)>J0;       
      I2(i-1,j-1)=I3(i-1,j-1)*2^7+I3(i-1,j)*2^6+I3(i-1,j+1)*2^5+I3(i,j+1)*2^4+I3(i+1,j+1)*2^3+I3(i+1,j)*2^2+I3(i+1,j-1)*2^1+I3(i,j-1)*2^0;

      end 
    end 
   
    axes(handles.axes12);
    imshow(I2);
    title('G -LBP Image');
    
    
% Texture Features
      
Mean11 = mean2(I2);
set(handles.edit13,'string',Mean11);
Standard_Deviation22 = std2(I2);
set(handles.edit14,'string',Standard_Deviation22 );
Entropy33 = entropy(I2);
set(handles.edit15,'string',Entropy33);
Kurtosis44 = kurtosis(double(I2(:)));
set(handles.edit16,'string',Kurtosis44);
Skewness55 = skewness(double(I2(:)));
set(handles.edit17,'string',Skewness55);



%% B Channel -Feature Extraction Using Local Binary Pattern
I21=J3;

  w=size(I21,1);     
   h=size(I21,2);  

%% LBP    
 scale = 2.^[7 6 5; 0 -inf 4; 1 2 3];  
    for i=2:w-1   
      for j=2:h-1    
         J0=I21(i,j);   
          I3(i-1,j-1)=I21(i-1,j-1)>J0;  
           I3(i-1,j)=I21(i-1,j)>J0;   
          I3(i-1,j+1)=I21(i-1,j+1)>J0;  
            I3(i,j+1)=I21(i,j+1)>J0;     
        I3(i+1,j+1)=I21(i+1,j+1)>J0;    
          I3(i+1,j)=I21(i+1,j)>J0;      
        I3(i+1,j-1)=I21(i+1,j-1)>J0;     
         I3(i,j-1)=I21(i,j-1)>J0;       
      I21(i-1,j-1)=I3(i-1,j-1)*2^7+I3(i-1,j)*2^6+I3(i-1,j+1)*2^5+I3(i,j+1)*2^4+I3(i+1,j+1)*2^3+I3(i+1,j)*2^2+I3(i+1,j-1)*2^1+I3(i,j-1)*2^0;

      end 
    end 
   
    axes(handles.axes13);
    imshow(I21);
    title('B -LBP Image');
    
    
% Texture Features
      
Mean111 = mean2(I21);
set(handles.edit28,'string',Mean111);
Standard_Deviation222 = std2(I21);
set(handles.edit29,'string',Standard_Deviation222 );
Entropy333 = entropy(I21);
set(handles.edit30,'string',Entropy333);
Kurtosis444 = kurtosis(double(I21(:)));
set(handles.edit31,'string',Kurtosis444);
Skewness555 = skewness(double(I21(:)));
set(handles.edit32,'string',Skewness555);

Mean=(Mean1+Mean11+Mean111)/3;
Standard_Deviation=(Standard_Deviation2+Standard_Deviation22+Standard_Deviation222)/3;
Entropy=(Entropy3+Entropy33+Entropy333)/3;
Kurtosis=(Kurtosis4+Kurtosis44+Kurtosis444)/3;
Skewness=(Skewness5+Skewness55+Skewness555)/3;


FF=[Mean Standard_Deviation Entropy Kurtosis Skewness];

save('Avg_Fea.mat','FF');

